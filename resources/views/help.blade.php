<h1>What differences are there from the original game?</h1>
<div class="well">
	<p>There are a few changes from the original game which enhance gameplay and prevent you getting stuck. The Japanese
		1.0 version is used as a base ROM because it allows use of some exclusive glitches in some of the advanced game
		modes. You may see some Japanese text but this will eventually all be removed.</p>
	<ul>
		<li>You no longer need the Lamp to push the bookshelf during the prologue and you can now see in
			dark rooms in the Sewers even without the Lamp (except in Open mode).</li>
		<li>You are never required to navigate through dark rooms so if you find yourself in total
			darkness then it’s guaranteed that you can find the Lamp elsewhere (in No Major Glitches).</li>
		<li>You can toggle between items which share the same slot in the menu by pressing Y. For example
			you can now hold both the Shovel and Flute and switch between them.</li>
		<li>The sub-menu for the Bottles no longer automatically opens and you can open it with X
			or toggle between them with Y.</li>
		<li>The water levels within Swamp Palace will always revert to being drained when you exit
			the overworld screen. This prevents you accidentally drowning keys underwater and getting stuck!</li>
		<li>The file select screen has a random row of symbols at the top. These are a unique identifier
			for each seed generated and do not have any other relevance.</li>
		<li>The Fat Faerie does not upgrade your Sword or Bow but her cave now contains two randomized
			chests which account for her usual upgrades having been shuffled into the mix.</li>
	</ul>
</div>

